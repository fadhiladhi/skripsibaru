package com.viss.pemesanan.Model;

public class PengirimanModel {
    String id,tglpemesanan,jasapengiriman,iduser,namaikan,status,noresi;

    public PengirimanModel() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTglpemesanan() {
        return tglpemesanan;
    }

    public void setTglpemesanan(String tglpemesanan) {
        this.tglpemesanan = tglpemesanan;
    }

    public String getJasapengiriman() {
        return jasapengiriman;
    }

    public void setJasapengiriman(String jasapengiriman) {
        this.jasapengiriman = jasapengiriman;
    }

    public String getIduser() {
        return iduser;
    }

    public void setIduser(String iduser) {
        this.iduser = iduser;
    }

    public String getNamaikan() {
        return namaikan;
    }

    public void setNamaikan(String namaikan) {
        this.namaikan = namaikan;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getNoresi() {
        return noresi;
    }

    public void setNoresi(String noresi) {
        this.noresi = noresi;
    }
}
